#include <iostream>
using namespace std;

int main() {


  char A = 65;
  char A_in_quotes = 'A';

  cout << "A=" << A << endl;
  cout << "A_in_quotes=" << A_in_quotes << endl;


  char a = 97;
  char a_in_quotes = 'a';

  cout << "a=" << a << endl;
  cout << "a_in_quotes=" << a_in_quotes << endl;

  char digit_1 = '1';
  cout << "digit_1 =" << digit_1 << endl;
  cout << "int(digit_1)=" << int(digit_1) << endl;

  return 0;
}
